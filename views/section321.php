<div class="container-restrictions">
    <div class="txt-restrictions">
        <h1>Restrictions</h1>
        <p>
            The daily restriction for Section 321 goods is one 
            shipment per individual or company per day. 
            Importers cannot consolidate multiple Section 
            321 shipments into one shipment, because the total 
            value will then exceed $800. Each Section 321 
            shipment must be manifested separately on the eManifest 
            with a unique shipment control numbers (SCN).
        </p>
        <p>
            Most goods not exceeding $800 in value can be released as 
            a Section 321 entry, with a few exceptions: 
            goods requiring inspection before release (regardless of value)
            ,goods subject to Anti-Dumping / Countervailing duty (ADD/CVD), 
            quota-class goods, and goods regulated by the Food and Drug Administration (FDA), 
            Food Safety Inspection Service (FSIS), National 
            Highway Transport and Safety Administration (NHTSA), 
            Consumer Product Safety Commission (CPSA), 
            and United States Department of Agriculture
            (USDA) can be released with Section 321 Entry Type 86. 
            CBP may refuse to clear a Section 321 shipment if 
            the shipment is deemed high risk for 
            a particular type of merchandise or class
        </p>

        <h1>What is Section 321?</h1>
        <p>
            A Section 321 entry is a type of informal entry that allows the release of goods valued at $800 or less. 
            To be released under a Section 321, the shipment must not exceed $800 in value and must not be one of several 
            lots covered by a single order or contract. Section 321 entries may be claimed in conjunction with an eManifest filing, 
            subsequently reducing paperwork for low-value goods and allow faster clearance processing.

            Approved in 2016, a de minimis shipment commonly referred to as Section 321, allows for goods valued at $800 USD or less,
            to enter duty-free into the U.S. Under this legislation they are permitted to enter without formal entry. Therefore, this regulation 
            is a great option for importers to save money and time.
        </p>
        <p>
            The Section 321 exemption allows the consignee to become the importer themselves, 
            hence bypassing the duty payable on items purchased internationally, or outside 
            of the United States. 
            The limit for this shipment is $800 USD, and only 1 shipment per day per consignee.
        </p>
